function rkpnKe(h, E, ν)
    function keFunc(e)
        a, b = ab(e)
        Ke = zeros(12, 12)
        D = E*h^3 / (12*(1 - ν^2))
        # Matrix Ke has 22 unique and negative unique entries
        Ke[1, 1] = Ke[10, 10] = Ke[7, 7] = Ke[4, 4] = (20(a^4) + 14(a^2)*(b^2) + 20(b^4) - 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1, 2] = Ke[10, 11] = (a^2 + 10(b^2) + 4(a^2)*ν) / (5(a^2)*b)
        Ke[1, 3] = Ke[4, 6] = (10(a^2) + b^2 + 4(b^2)*ν) / (5a*(b^2))
        Ke[7, 10] = Ke[1, 4] = (10(a^4) - 14(a^2)*(b^2) - 20(b^4) + 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[8, 10] = Ke[1, 5] = (a^2 + 10(b^2) - (a^2)*ν) / (5(a^2)*b)
        Ke[1, 6] = Ke[3, 4] = (5(a^2) - (b^2) - 4(b^2)*ν) / (5a*(b^2))
        Ke[4, 10] = Ke[1, 7] = (-10(a^4) + 14(a^2)*(b^2) - 10(b^4) - 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[5, 10] = Ke[1, 8] = (-(a^2) + 5(b^2) + (a^2)*ν) / (5(a^2)*b)
        Ke[1, 9] = Ke[4, 12] = (5(a^2) - (b^2) + (b^2)*ν) / (5a*(b^2))
        Ke[4, 7] = Ke[1, 10] = (-20(a^4) - 14(a^2)*(b^2) + 10(b^4) + 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1, 11] = Ke[2, 10] = (-(a^2) + 5(b^2) - 4(a^2)*ν) / (5(a^2)*b)
        Ke[1, 12] = Ke[4, 9] = (10(a^2) + b^2 - (b^2)*ν) / (5a*(b^2))
        Ke[5, 5] = Ke[2, 2] = Ke[11, 11] = Ke[8, 8] = (4(a^2) + 20(b^2) - 4(a^2)*ν) / (15a*b)
        Ke[8, 9] = Ke[2, 3] = ν
        Ke[2, 4] = Ke[7, 11] = -Ke[8, 10]
        Ke[2, 5] = Ke[8, 11] = (-(a^2) + 10(b^2) + (a^2)*ν) / (15a*b)
        Ke[6, 8] = Ke[8, 12] = Ke[5, 9] = Ke[3, 8] = Ke[2, 6] = Ke[6, 11] = Ke[9, 11] = Ke[2, 12] = Ke[3, 5] = Ke[3, 11] = Ke[5, 12] = Ke[2, 9] = 0
        Ke[4, 11] = Ke[2, 7] = -Ke[5, 10]
        Ke[2, 8] = Ke[5, 11] = (a^2 + 5(b^2) - (a^2)*ν) / (15a*b)
        Ke[2, 11] = Ke[5, 8] = (-4(a^2) + 10(b^2) + 4(a^2)*ν) / (15a*b)
        Ke[12, 12] = Ke[9, 9] = Ke[3, 3] = Ke[6, 6] = (20(a^2) + 4(b^2) - 4(b^2)*ν) / (15a*b)
        Ke[3, 6] = Ke[9, 12] = (10(a^2) - 4(b^2) + 4(b^2)*ν) / (15a*b)
        Ke[6, 10] = Ke[3, 7] = -Ke[1, 9]
        Ke[3, 9] = Ke[6, 12] = (5(a^2) + b^2 - (b^2)*ν) / (15a*b)
        Ke[6, 7] = Ke[3, 10] = -Ke[1, 12]
        Ke[3, 12] = Ke[6, 9] = (10(a^2) - (b^2) + (b^2)*ν) / (15a*b)
        Ke[4, 5] = Ke[7, 8] = -Ke[1, 2]
        Ke[4, 8] = Ke[5, 7] = -Ke[1, 11]
        Ke[5, 6] = Ke[11, 12] = -Ke[8, 9]
        Ke[10, 12] = Ke[7, 9] = -Ke[1, 3]
        Ke[9, 10] = Ke[7, 12] = -Ke[1, 6]
        return D*Symmetric(Ke)
    end
    return keFunc
end

function rkpnRe(q)
    function reFunc(e)
        a, b = ab(e)
        re = zeros(12)
        re[1] = (1//4)*a*b
        re[2] = (1//24)*(a^2)*b
        re[3] = (1//24)*a*(b^2)
        re[4] = (1//4)*a*b
        re[5] = (-1//24)*(a^2)*b
        re[6] = (1//24)*a*(b^2)
        re[7] = (1//4)*a*b
        re[8] = (-1//24)*(a^2)*b
        re[9] = (-1//24)*a*(b^2)
        re[10] = (1//4)*a*b
        re[11] = (1//24)*(a^2)*b
        re[12] = (-1//24)*a*(b^2)
        return q*re
    end
    return reFunc
end
