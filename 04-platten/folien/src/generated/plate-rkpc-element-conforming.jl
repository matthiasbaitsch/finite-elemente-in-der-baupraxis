function rkpcRe(q)
    function reFunc(e)
        a, b = ab(e)
        re = zeros(16)
        re[1] = (1//4)*a*b
        re[2] = (1//24)*(a^2)*b
        re[3] = (1//24)*a*(b^2)
        re[4] = (1//144)*(a^2)*(b^2)
        re[5] = (1//4)*a*b
        re[6] = (-1//24)*(a^2)*b
        re[7] = (1//24)*a*(b^2)
        re[8] = (-1//144)*(a^2)*(b^2)
        re[9] = (1//4)*a*b
        re[10] = (-1//24)*(a^2)*b
        re[11] = (-1//24)*a*(b^2)
        re[12] = (1//144)*(a^2)*(b^2)
        re[13] = (1//4)*a*b
        re[14] = (1//24)*(a^2)*b
        re[15] = (-1//24)*a*(b^2)
        re[16] = (-1//144)*(a^2)*(b^2)
        return q*re
    end
    return reFunc
end
