function rrmKe(h, E, ν)
    function keFunc(e)
        a, b = ab(e)
        Ke = zeros(12, 12)
        D = E*h^3 / (12*(1 - ν^2))
        # Matrix Ke has 24 unique and negative unique entries
        Ke[1, 1] = Ke[10, 10] = Ke[7, 7] = Ke[4, 4] = (5(a^2) + 5(b^2) - 5(a^2)*ν - 5(b^2)*ν) / (3a*b*(h^2))
        Ke[1, 2] = Ke[10, 11] = Ke[8, 10] = Ke[1, 5] = (-5b*(1 - ν)) / (6(h^2))
        Ke[1, 12] = Ke[1, 3] = Ke[4, 6] = Ke[4, 9] = (-5a*(1 - ν)) / (6(h^2))
        Ke[7, 10] = Ke[1, 4] = (5(a^2) - 10(b^2) - 5(a^2)*ν + 10(b^2)*ν) / (6a*b*(h^2))
        Ke[1, 6] = Ke[1, 9] = Ke[3, 4] = Ke[4, 12] = (-5a*(1 - ν)) / (12(h^2))
        Ke[4, 10] = Ke[1, 7] = (-5(a^2) - 5(b^2) + 5(a^2)*ν + 5(b^2)*ν) / (6a*b*(h^2))
        Ke[5, 10] = Ke[1, 11] = Ke[2, 10] = Ke[1, 8] = (-5b*(1 - ν)) / (12(h^2))
        Ke[4, 7] = Ke[1, 10] = (-10(a^2) + 5(b^2) + 10(a^2)*ν - 5(b^2)*ν) / (6a*b*(h^2))
        Ke[5, 5] = Ke[2, 2] = Ke[11, 11] = Ke[8, 8] = (10(a^2)*(b^2) + 3(a^2)*(h^2) + 6(b^2)*(h^2) - 10(a^2)*(b^2)*ν - 3(a^2)*(h^2)*ν) / (18a*b*(h^2))
        Ke[2, 3] = (1//2)*(-1 - ν) + (5//8)*(1 + ν)
        Ke[2, 4] = Ke[4, 5] = Ke[7, 8] = Ke[7, 11] = -Ke[1, 2]
        Ke[2, 5] = Ke[8, 11] = (10(a^2)*(b^2) + 3(a^2)*(h^2) - 12(b^2)*(h^2) - 10(a^2)*(b^2)*ν - 3(a^2)*(h^2)*ν) / (36a*b*(h^2))
        Ke[3, 11] = Ke[2, 6] = (1//2) + (1//8)*(-1 - ν) + (1//2)*(-1 + ν)
        Ke[4, 8] = Ke[4, 11] = Ke[5, 7] = Ke[2, 7] = -Ke[5, 10]
        Ke[2, 8] = Ke[5, 11] = (5(a^2)*(b^2) - 3(a^2)*(h^2) - 6(b^2)*(h^2) - 5(a^2)*(b^2)*ν + 3(a^2)*(h^2)*ν) / (36a*b*(h^2))
        Ke[3, 8] = Ke[2, 9] = (1//4)*(-1 + ν) - (1//2)*ν + (1//8)*(1 + ν)
        Ke[2, 11] = Ke[5, 8] = (5(a^2)*(b^2) - 3(a^2)*(h^2) + 3(b^2)*(h^2) - 5(a^2)*(b^2)*ν + 3(a^2)*(h^2)*ν) / (18a*b*(h^2))
        Ke[2, 12] = Ke[3, 5] = (1//8)*(-1 - ν) - (1//2)*ν + (1//4)*(1 + ν)
        Ke[12, 12] = Ke[9, 9] = Ke[3, 3] = Ke[6, 6] = (10(a^2)*(b^2) + 6(a^2)*(h^2) + 3(b^2)*(h^2) - 10(a^2)*(b^2)*ν - 3(b^2)*(h^2)*ν) / (18a*b*(h^2))
        Ke[3, 6] = Ke[9, 12] = (5(a^2)*(b^2) + 3(a^2)*(h^2) - 3(b^2)*(h^2) - 5(a^2)*(b^2)*ν + 3(b^2)*(h^2)*ν) / (18a*b*(h^2))
        Ke[6, 10] = Ke[3, 7] = Ke[9, 10] = Ke[7, 12] = -Ke[1, 6]
        Ke[3, 9] = Ke[6, 12] = (5(a^2)*(b^2) - 6(a^2)*(h^2) - 3(b^2)*(h^2) - 5(a^2)*(b^2)*ν + 3(b^2)*(h^2)*ν) / (36a*b*(h^2))
        Ke[6, 7] = Ke[10, 12] = Ke[3, 10] = Ke[7, 9] = -Ke[1, 12]
        Ke[3, 12] = Ke[6, 9] = (10(a^2)*(b^2) - 12(a^2)*(h^2) + 3(b^2)*(h^2) - 10(a^2)*(b^2)*ν - 3(b^2)*(h^2)*ν) / (36a*b*(h^2))
        Ke[5, 6] = Ke[11, 12] = (1//4)*(-1 - ν) + (1//8)*(1 + ν)
        Ke[8, 12] = Ke[5, 9] = (1//8)*(-1 - ν) + (1//2)*ν
        Ke[6, 11] = Ke[8, 9] = Ke[5, 12] = (1//8)*(1 + ν)
        Ke[6, 8] = Ke[9, 11] = (1//8)*(-1 - ν) + (1//4)*(1 - ν)
        return D*Symmetric(Ke)
    end
    return keFunc
end

function rrmRe(q)
    function reFunc(e)
        a, b = ab(e)
        re = zeros(12)
        re[1] = (1//4)*a*b
        re[2] = 0.0
        re[3] = 0.0
        re[4] = (1//4)*a*b
        re[5] = 0.0
        re[6] = 0.0
        re[7] = (1//4)*a*b
        re[8] = 0.0
        re[9] = 0.0
        re[10] = (1//4)*a*b
        re[11] = 0.0
        re[12] = 0.0
        return q*re
    end
    return reFunc
end
